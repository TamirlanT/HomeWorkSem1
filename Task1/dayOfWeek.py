n = int(input('Введи число n = '))
if n == 1 : print ('Понедельник - будний')
if n == 2 : print ('Вторник - будний')
if n == 3 : print ('Среда - будний')
if n == 4 : print ('Четверг - будний')
if n == 5 : print ('Пятница - будний')
if n == 6 : print ('Суббота - выходной')
if n == 7 : print ('Воскресенье - выходной')
if n > 7 or n < 0 : print ('Некоректное число')